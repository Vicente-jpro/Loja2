    private void loginJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJBActionPerformed

        if (!campoDeTextoVazio()) {

            int idUser = 0;

            userConnectiondb = new UsuarioConnectiondb();

            String nomeUsuario = userNameJT.getText();

            
          
            String password = passwordJT.getText();

            usuario = new Usuario(nomeUsuario, password);
            
            idUser = userConnectiondb.validarUsuario( usuario  );
            // idUser != 0; significa que este usuario existe, entao fecha a tela de login
            if ( idUser != 0 ) {
                
                setVisible(false);
                
     

                currenteUser = new CurrentUserConnectiondb();
                
                // Elimina a tabela do usuario anterior
                currenteUser.drop_table();
                
                // Cria uma nova tabla de usúario corrente
                currenteUser.create_table();

                // Adiciona o usuario corrente da aplicaçao 
                currenteUser.savarUsuario(idUser);
                new CompraForm().setVisible ( true ); //Janela de compra ou de vendas
                
                
                // Obter as horas actuais
                time = new CurrentDateAndTime();
                String dataHoraEntrada = time.getCurrentDate()+" "+time.getCurrentHour();
                
                // Define a hora de entrada e adiciona o usuario corrente no tempo de trabalho
                tempoTrabalho = new TempoTrabalho(dataHoraEntrada, null);
                tempoTrabalho.setIdUsuario(idUser);
                
                // salvar os dados no DB
                tempoTrabalhodb = new TempoTrabalhodb();
                tempoTrabalhodb.createHoraEntrada(tempoTrabalho);
                
                currenteUser = null;
                time = null;
                tempoTrabalho = null;
                
            }

        } else {
            JOptionPane.showMessageDialog(null, "Os campos de texto nao podem estar vazios",
                    "ERRO AO ACESSAR A APLICAÇAO", JOptionPane.ERROR_MESSAGE);
        }

        limparTexto();

    }//GEN-LAST:event_loginJBActionPerformed

    private void novoAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_novoAdminActionPerformed

        usuariodb = new Usuariodb();

        // Verifica se App já foi usada pela primeira vez.
        /* A App é usada pela primeira vez quando o admin ja fez um cadastro.
        Uma vez feita o cadastro dos administrador o App já não pode permitir
        o cadastro de Administradores 
        */
        boolean existeAdmin = usuariodb.verificaExistenciaUsuario();

        if ( existeAdmin ) {
            
          int opcao =   JOptionPane.showConfirmDialog(null, "Ja existe um administrador cadastrado.\n\n"
                  + "Clica em YES para entrar como administrador.",
                  "Administrador", JOptionPane.YES_NO_OPTION );
          
          if (opcao == 0)
              new LoginAdmin( "menu","Administrador" ).setVisible( true );
        
        } else {
            new UsuarioForm().setVisible(true);
        }

    }//GEN-LAST:event_novoAdminActionPerformed

