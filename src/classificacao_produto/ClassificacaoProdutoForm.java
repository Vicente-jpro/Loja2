    @Override
    public void ReadDataCombobox() {

        classificacaoProdutodb = new ClassificacaoProdutodb();

        for (Object element : classificacaoProdutodb.read()) {

            nomeDeClasseJC.addItem(element);
        }

    }

    private boolean itemClasseProdutoSelecionada() {

        return nomeDeClasseJC.getSelectedIndex() > 0; // true
    }

    private boolean campoNomeDaClasseVazioJT() {

        return nomeDaClassJT.getText().isEmpty() || "Nome da Classe".equals(nomeDaClassJT.getText()); // true
    }

