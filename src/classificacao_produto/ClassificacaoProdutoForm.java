    private void salvarJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJBActionPerformed

        if (campoNomeDaClasseVazioJT()) {

            JOptionPane.showMessageDialog(null, "O campo de texto não pode estar vazio",
                    "SALVAR CLASSE DE PRODUTO", JOptionPane.ERROR_MESSAGE);

        } else {

             classificacaoProdutodb = new ClassificacaoProdutodb();
            // pesquisar a classe de produto na lista    
            String classeProduto = nomeDaClassJT.getText();
           
             String classe = transform.toUpperCaseAllFirsLetter(classeProduto);

             //verifica se o elemento existe na lista
            boolean existeClassificacao = classificacaoProdutodb.contem(classe);

            
            // verifica se o elemento foi 
            if (existeClassificacao) {
                JOptionPane.showMessageDialog(null, "Essa classificação já existe",
                        "SALVAR CLASSE DE PRODUTO", JOptionPane.ERROR_MESSAGE);
            } else {
                classificacaoProdutoConnectiondb = new ClassificacaoProdutoConnectiondb();


                classificacaoProdutoConnectiondb.salvar(classe);
                nomeDeClasseJC.addItem(classe);

            }
            
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_salvarJBActionPerformed

    private void actualizarJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarJBActionPerformed

        if (!campoNomeDaClasseVazioJT() && !itemClasseProdutoSelecionada()) {

            JOptionPane.showMessageDialog(null, "O campo de Texto e a caixa de Classe de Produto não pode estar vazia",
                    "ACTUALIZAR CLASSE DE PRODUTO", JOptionPane.ERROR_MESSAGE);

        } else {

            classificacaoProdutoConnectiondb = new ClassificacaoProdutoConnectiondb();

            String novaClasse = nomeDaClassJT.getText();
            String antigaClasse = nomeDeClasseJC.getSelectedItem().toString();

            String novaclass = transform.toUpperCaseAllFirsLetter(novaClasse);

            classificacaoProdutoConnectiondb.actualizar(novaclass, antigaClasse);
            nomeDeClasseJC.addItem(novaClasse);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_actualizarJBActionPerformed

