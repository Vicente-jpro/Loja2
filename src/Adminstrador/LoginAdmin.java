    private boolean campoDeTextoVazio() {
        return "".equals(passwordJT.getText()) || "".equals(userNameJT.getText()); //true
    }

    private void limparTexto() {

        userNameJT.setText("");
        passwordJT.setText("");
    }

    private void abrirJFrameComoAdmin() {

        switch (JFrameName) {

            case "menu":
                new CompraForm().setVisible(true); // Janela de compra ou vendas
                break;
            case "usuarios":
                new UsuarioForm().setVisible(true);
                break;
            case "produto":
                new ProdutoCadastroForm().setVisible(true);
                break;

            default: // desenvolvedor

                break;

        }

    }

    // Abrir a janela de pacotes ou de cadastro de funcionários
    private void desenvolvedor() {

        usuariodb = new Usuariodb();
        boolean isDesenvolvedor = usuariodb.isDesenvolvedor(usuario);
        //System.out.println("isDeveloper " + isDesenvolvedor);
        if (isDesenvolvedor) {

            setVisible(false);

            if (JFrameName.equals("desenvolvedor")) {
                
                setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                new EscolhaDoPacote().setVisible(true);
                setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            
            } else {
                
                setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                new UsuarioForm().setVisible(true);
                
                setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            }

        }

    }
