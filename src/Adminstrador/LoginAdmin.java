    private void loginJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJBActionPerformed

        if (!campoDeTextoVazio()) {

            usuarioConnectiondb = new UsuarioConnectiondb();

            String userName = userNameJT.getText();
            String password = passwordJT.getText();
            usuario = new Usuario(userName, password);

            boolean isAdmin = usuarioConnectiondb.validarAdmininistrador(usuario);
            // isAdmin == true; significa que este usuario existe, entao fecha a tela de login

            // System.out.println("isAdming " + isAdmin);
            if (isAdmin) {
                setVisible(false);

                setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                abrirJFrameComoAdmin();
                setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

            } else {
                desenvolvedor(); // Abrir a janela de pacotes ou de cadastro de funcionários
            }

        } else {
            JOptionPane.showMessageDialog(null, "Os campos de texto nao podem estar vazios",
                    "ERRO AO ACESSAR A APLICAÇAO", JOptionPane.ERROR_MESSAGE);
        }

        limparTexto();

    }//GEN-LAST:event_loginJBActionPerformed

    private void novoAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_novoAdminActionPerformed

        usuariodb = new Usuariodb();

        String nomeDoAdmin = userNameJT.getText();
        String password = passwordJT.getText();

        usuario = new Usuario(nomeDoAdmin, password);

        int existeAdmin = usuariodb.verificaExistenciaUsuario(usuario);

        if (existeAdmin != 0) {
            JOptionPane.showMessageDialog(null, "Ja existe um usuario cadastrado",
                    "Usuario", JOptionPane.INFORMATION_MESSAGE);

            int opcao = JOptionPane.showConfirmDialog(null, "Clica em YES para entrar como usuario", "Usuario", JOptionPane.YES_NO_OPTION);

            if (opcao == 0) {
                new LoginAdmin("menu", "Usuario").setVisible(true);
            }

        } else {
            new UsuarioForm().setVisible(true);
        }

    }//GEN-LAST:event_novoAdminActionPerformed

